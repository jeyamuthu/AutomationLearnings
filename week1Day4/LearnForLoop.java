package week1Day4;

public class LearnForLoop {

	public static void main(String[] args) {
		
		// Print the value  ascending order 0 to 9 
		for(int i=0;i<=9;i++)
		{
			System.out.print(i+" ");
		}
		// to create the next empty line
		System.out.println();
		// print the value descending order 9 to 0
		for(int i=9;i>=0;i--)
		{
			System.out.print(i+" ");
		}
	}
}
